package variable;

public class Var5 {

    public static void main(String[] args) {
        int a; //선언, 초기화 별도
        a = 1;
        System.out.println(a);

        int b = 20; //선언 및 초기화 한번에
        System.out.println(b);

        int c, d;
        c = 3;
        d = 5;
        System.out.println(c);
        System.out.println(d);
    }
}
