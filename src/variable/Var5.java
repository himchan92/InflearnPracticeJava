package variable;

public class Var5 {
    public static void main(String[] args) {
        int a; //변수선언
        a = 1;
        System.out.println(a);

        int b = 2; //선언 동시에 초기화 가능
        System.out.println(b);

        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
