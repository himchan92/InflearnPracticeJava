package 자바입문.operator;

public class Comp1 {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;

        System.out.println(a == b); //같은지
        System.out.println(a != b); //다른지
        System.out.println(a > b); //a가 큰지
        System.out.println(a < b); //b가 큰지
        System.out.println(a >= b); //a가 같거나 큰지
        System.out.println(a <= b); //b가 같거나 큰지

    }
}
