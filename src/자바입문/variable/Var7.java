package 자바입문.variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100; //정수
        double b = 10.5; //실수
        boolean c = true; //불리언
        char d = 'A'; //문자 하나, 단일따옴표
        String e = "Hello Java"; //문자열 다루는 참조타입, 쌍따옴표

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
